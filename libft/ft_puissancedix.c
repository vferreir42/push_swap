long long		ft_puissancedix(long long n)
{
	int		i;
	int		nb;

	if (n > 9 || n < 0)
		return (0);
	nb = 1;
	i = 0;
	while (i < n)
	{
		nb *= 10;
		i++;
	}
	return (nb);
}

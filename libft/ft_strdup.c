#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*new;

	i = ft_strlen(s);
	if (!(new = (char*)malloc(sizeof(char) * (i + 1))))
		return (0);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}

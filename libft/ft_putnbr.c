#include "libft.h"

void			ft_putnbr(long long n)
{
	int i;

	i = 0;
	if (n < 0)
	{
		i++;
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
